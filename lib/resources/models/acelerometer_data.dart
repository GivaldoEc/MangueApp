class AcelerometerData {
  final int accX;
  final int accY;
  final int accZ;
  final int dpsX;
  final int dpsY;
  final int dpsZ;

  const AcelerometerData({
    required this.accX,
    required this.accY,
    required this.accZ,
    required this.dpsX,
    required this.dpsY,
    required this.dpsZ,
  });
}
